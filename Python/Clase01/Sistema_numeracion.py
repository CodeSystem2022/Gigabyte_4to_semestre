#Profundizamos en los sistemas de numeracion

#Sistema decimal
a =10
print(f"a decimal: {a}")

#Sistema vinario
a = 0b1010 #0b y seguido el numero que queramos en binario
print(f"a binario: {a}")

#Sistema octal
a = 0o12 #0o y seguido el numero que queramos en octal
print(f"a octal: {a}")

#Sistema hexadecimal
a=0xA#0x y el numero o letra que queramos en hexadecimal
print(f"a hexadecimal: {a}")

#base decimal
